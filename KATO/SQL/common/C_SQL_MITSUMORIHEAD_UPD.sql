MERGE INTO [KATO].[dbo].見積ヘッド AS A
USING
    (
        SELECT
            @p0 AS 見積書番号
           ,@p1 AS 見積年月日
           ,@p2 AS 標題
           ,@p3 AS 担当者名
           ,@p4 AS 納期
           ,@p5 AS 支払条件
           ,@p6 AS 有効期限
           ,@p7 AS 備考
           ,@p8 AS 得意先コード
           ,@p9 AS 得意先名称
           ,@p10 AS 担当者コード
           ,@p11 AS 営業所コード
           ,@p12 AS 売上金額
           ,@p13 AS 粗利額
           ,@p14 AS 納入先コード
           ,@p15 AS 納入先名称
           ,@p16 AS 削除
           ,@p17 AS 登録日時
           ,@p18 AS 登録ユーザー名
           ,@p19 AS 更新日時
           ,@p20 AS 更新ユーザー名
    ) AS B
ON
    (
        A.見積書番号 = B.見積書番号
    )
WHEN MATCHED THEN
    UPDATE
    SET
        見積年月日 = B.見積年月日
       ,標題 = B.標題
       ,担当者名 = B.担当者名
       ,納期 = B.納期
       ,支払条件 = B.支払条件
       ,有効期限 = B.有効期限
       ,備考 = B.備考
       ,得意先コード = B.得意先コード
       ,得意先名称 = B.得意先名称
       ,担当者コード = B.担当者コード
       ,営業所コード = B.営業所コード
       ,売上金額 = B.売上金額
       ,粗利額 = B.粗利額
       ,納入先コード = B.納入先コード
       ,納入先名称 = B.納入先名称
       ,削除 = B.削除
       ,更新日時 = B.更新日時
       ,更新ユーザー名 = B.更新ユーザー名
WHEN NOT MATCHED THEN
    INSERT(
        見積書番号
       ,見積年月日
       ,標題
       ,担当者名
       ,納期
       ,支払条件
       ,有効期限
       ,備考
       ,得意先コード
       ,得意先名称
       ,担当者コード
       ,営業所コード
       ,売上金額
       ,粗利額
       ,納入先コード
       ,納入先名称
       ,削除
       ,登録日時
       ,登録ユーザー名
       ,更新日時
       ,更新ユーザー名
    )
    VALUES
    (
	    B.見積書番号
       ,B.見積年月日
       ,B.標題
       ,B.担当者名
       ,B.納期
       ,B.支払条件
       ,B.有効期限
       ,B.備考
       ,B.得意先コード
       ,B.得意先名称
       ,B.担当者コード
       ,B.営業所コード
       ,B.売上金額
       ,B.粗利額
       ,B.納入先コード
       ,B.納入先名称
       ,B.削除
       ,B.登録日時
       ,B.登録ユーザー名
       ,B.更新日時
       ,B.更新ユーザー名
    )
;