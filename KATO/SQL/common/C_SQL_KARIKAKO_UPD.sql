    INSERT(
        仕入先コード
       ,発注年月日
       ,発注番号
       ,発注者コード
       ,営業所コード
       ,担当者コード
       ,受注番号
       ,出庫番号
       ,行番号
       ,商品コード
       ,メーカーコード
       ,大分類コード
       ,中分類コード
       ,Ｃ１
       ,Ｃ２
       ,Ｃ３
       ,Ｃ４
       ,Ｃ５
       ,Ｃ６
       ,発注数量
       ,発注単価
       ,発注金額
       ,納期
       ,発注フラグ
       ,注番
       ,仕入済数量
       ,印刷フラグ
       ,加工区分
       ,仕入先名称
       ,削除
       ,登録日時
       ,登録ユーザー名
       ,更新日時
       ,更新ユーザー名
    )
    VALUES
    (
	    B.仕入先コード
       ,B.発注年月日
       ,B.発注番号
       ,B.発注者コード
       ,B.営業所コード
       ,B.担当者コード
       ,B.受注番号
       ,B.出庫番号
       ,B.行番号
       ,B.商品コード
       ,B.メーカーコード
       ,B.大分類コード
