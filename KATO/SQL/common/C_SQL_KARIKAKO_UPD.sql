USING
    (
        SELECT
            @0 AS 仕入先コード
           ,@1 AS 発注年月日
           ,@2 AS 発注番号
           ,@3 AS 発注者コード
           ,@4 AS 営業所コード
           ,@5 AS 担当者コード
           ,@6 AS 受注番号
           ,@7 AS 出庫番号
           ,@8 AS 行番号
           ,@9 AS 商品コード
           ,@10 AS メーカーコード
