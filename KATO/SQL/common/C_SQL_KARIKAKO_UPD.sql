       ,受注番号 = B.受注番号
       ,出庫番号 = B.出庫番号
       ,行番号 = B.行番号
       ,商品コード = B.商品コード
       ,メーカーコード = B.メーカーコード
       ,大分類コード = B.大分類コード
       ,中分類コード = B.中分類コード
       ,Ｃ１ = B.Ｃ１
       ,Ｃ２ = B.Ｃ２
       ,Ｃ３ = B.Ｃ３
       ,Ｃ４ = B.Ｃ４
       ,Ｃ５ = B.Ｃ５
       ,Ｃ６ = B.Ｃ６
       ,発注数量 = B.発注数量
       ,発注単価 = B.発注単価
       ,発注金額 = B.発注金額
       ,納期 = B.納期
       ,発注フラグ = B.発注フラグ
       ,注番 = B.注番
       ,仕入済数量 = B.仕入済数量
       ,印刷フラグ = B.印刷フラグ
       ,加工区分 = B.加工区分
       ,仕入先名称 = B.仕入先名称
       ,削除 = B.削除
       ,更新日時 = B.更新日時
       ,更新ユーザー名 = B.更新ユーザー名
