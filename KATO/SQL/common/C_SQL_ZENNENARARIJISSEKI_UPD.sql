MERGE INTO [KATO].[dbo].前年粗利実績 AS A
USING
    (
        SELECT
            @0 AS 取引先コード
           ,@1 AS 金額１
           ,@2 AS 金額２
           ,@3 AS 金額３
           ,@4 AS 金額４
           ,@5 AS 金額５
           ,@6 AS 金額６
           ,@7 AS 金額７
           ,@8 AS 金額８
           ,@9 AS 金額９
           ,@10 AS 金額１０
           ,@11 AS 金額１１
           ,@12 AS 金額１２
    ) AS B
ON
    (
        A.取引先コード = B.取引先コード
    )
WHEN MATCHED THEN
    UPDATE
    SET
        金額１ = B.金額１
       ,金額２ = B.金額２
       ,金額３ = B.金額３
       ,金額４ = B.金額４
       ,金額５ = B.金額５
       ,金額６ = B.金額６
       ,金額７ = B.金額７
       ,金額８ = B.金額８
       ,金額９ = B.金額９
       ,金額１０ = B.金額１０
       ,金額１１ = B.金額１１
       ,金額１２ = B.金額１２
WHEN NOT MATCHED THEN
    INSERT(
        取引先コード
       ,金額１
       ,金額２
       ,金額３
       ,金額４
       ,金額５
       ,金額６
       ,金額７
       ,金額８
       ,金額９
       ,金額１０
       ,金額１１
       ,金額１２
    )
    VALUES
    (
	    B.取引先コード
       ,B.金額１
       ,B.金額２
       ,B.金額３
       ,B.金額４
       ,B.金額５
       ,B.金額６
       ,B.金額７
       ,B.金額８
       ,B.金額９
       ,B.金額１０
       ,B.金額１１
       ,B.金額１２
    )
;